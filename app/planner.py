from __future__ import annotations


class TaskPlanner:
    def build_plan(self, request: str, task_type: str) -> list[str]:
        if task_type == "code":
            return [
                "فهم الطلب وتحديد ملفاته أو مدخلاته الأساسية.",
                "تقسيم التنفيذ إلى أجزاء صغيرة يمكن التحقق منها.",
                "تنفيذ التعديل أو إنشاء الملفات المطلوبة.",
                "تشغيل فحص سريع أو مراجعة المخرجات.",
                "تلخيص ما تم وما الذي يمكن تحسينه لاحقاً.",
            ]
        if task_type == "vision":
            return [
                "فحص الصورة وتحديد العناصر المهمة.",
                "ربط العناصر بسؤال المستخدم.",
                "استخراج الملاحظات أو النصوص أو الأخطاء المرئية.",
                "تقديم خلاصة عملية واقتراح الخطوة التالية.",
            ]
        return [
            "تحديد الهدف النهائي من الطلب.",
            "جمع المعلومات المتاحة من الذاكرة أو الملفات.",
            "تنفيذ الإجابة أو المهمة على مراحل قصيرة.",
            "مراجعة النتيجة واقتراح التحسينات التالية.",
        ]

    def build_workflow(self, request: str, task_type: str, mode: str) -> list[dict[str, str]]:
        if mode == "manager":
            return [
                {"title": "تحليل المتطلبات", "details": "تحويل الطلب إلى نطاق عمل واضح ومخرجات متوقعة."},
                {"title": "تفكيك المشروع", "details": "تقسيم المشروع إلى وحدات ومراحل قصيرة."},
                {"title": "اقتراح الملفات الأساسية", "details": "تحديد الملفات أو المجلدات التي يجب إنشاؤها."},
                {"title": "خطة التنفيذ التالية", "details": "وضع أولويات التنفيذ وأقرب خطوة عملية."},
            ]
        if task_type == "code":
            return [
                {"title": "فهم المشكلة", "details": "تحليل الطلب وتحديد نوع التعديل أو المشروع."},
                {"title": "استكشاف مساحة العمل", "details": "البحث عن الملفات ذات الصلة أو إنشاء هيكل جديد."},
                {"title": "إنتاج المخرجات", "details": "إنشاء ملفات أولية أو اقتراح التغييرات المطلوبة."},
                {"title": "التحقق السريع", "details": "مراجعة النتيجة وإبراز المخاطر أو الأعمال التالية."},
            ]
        return [
            {"title": "فهم الطلب", "details": "تلخيص الهدف النهائي للمهمة."},
            {"title": "تجهيز السياق", "details": "جمع المصادر أو الملاحظات المساندة."},
            {"title": "صياغة النتيجة", "details": "إنتاج جواب أو مخرجات قابلة للاستخدام."},
            {"title": "الخطوة التالية", "details": "اقتراح المتابعة العملية الأقرب."},
        ]
